  sort_by <- c(
    "refl_data_param",
    "refl",
    "quant_report_refl",
    "spec_no_atm_comp_data_param",
    "spec_no_atm_comp",
    "spec_data_param",
    "spec",
    "sc_sample_data_param",
    "sc_sample",
    "sc_ref_data_param",
    "sc_ref",
    "optics",
    "optics_ref",
    "acquisition_ref",
    "fourier_transformation_ref",
    "fourier_transformation",
    "sample",
    "acquisition",
    "instrument_ref",
    "instrument",
    "lab_and_process_param",
    "lab_and_process_param_1",
    "lab_and_process_param_2",
    "info_block",
    "history"
  )

  to_sort_by <- intersect(sort_by, names(dataset_list))

  dataset_list <- dataset_list[to_sort_by]

  return(dataset_list)
}
